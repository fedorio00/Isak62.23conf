; Тестовая программа для выполнения bitreverse над вектором длины 8
; Вектор располагается с адреса 100
; Результат будет записан с того же адреса 100

; Загружаем тестовые данные
LOAD 100, 123456    ; элемент 1
LOAD 101, 789012    ; элемент 2
LOAD 102, 345678    ; элемент 3
LOAD 103, 901234    ; элемент 4
LOAD 104, 567890    ; элемент 5
LOAD 105, 432109    ; элемент 6
LOAD 106, 876543    ; элемент 7
LOAD 107, 210987    ; элемент 8

; Выполняем bitreverse для каждого элемента
BITREV 100, 100
BITREV 101, 101
BITREV 102, 102
BITREV 103, 103
BITREV 104, 104
BITREV 105, 105
BITREV 106, 106
BITREV 107, 107
